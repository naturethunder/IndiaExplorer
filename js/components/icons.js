/**
 * Build an inline SVG icon string.
 * opts.size   — px width/height (default 24; ignored if opts.cls sizes it)
 * opts.cls    — extra class(es) on the <svg> (e.g. 'w-5 h-5 text-primary')
 * opts.fill   — true → fill with currentColor (for star/heart badges),
 *               otherwise stroked outline (default)
 * opts.stroke — stroke width (default 2)
 * opts.label  — accessible label; when set, role="img" + <title>, else aria-hidden
 */
export function icon(name, opts = {}) {
  const body = ICONS[name];
  if (!body) return '';
  const size = opts.size || 24;
  const hasCls = !!opts.cls;
  const dims = hasCls ? '' : ' width="' + size + '" height="' + size + '"';
  const cls = hasCls ? ' class="' + opts.cls + '"' : '';
  const fill = opts.fill ? 'currentColor' : 'none';
  const stroke = opts.fill ? 'none' : 'currentColor';
  const sw = opts.stroke != null ? opts.stroke : 2;
  const a11y = opts.label
    ? ' role="img" aria-label="' + String(opts.label).replace(/"/g, '&quot;') + '"'
    : ' aria-hidden="true" focusable="false"';
  return (
    '<svg xmlns="http://www.w3.org/2000/svg"' + dims + cls + a11y +
    ' viewBox="0 0 24 24" fill="' + fill + '" stroke="' + stroke + '"' +
    ' stroke-width="' + sw + '" stroke-linecap="round" stroke-linejoin="round">' +
    body + '</svg>'
  );
}

/** True if an icon name exists in the set. */
export function hasIcon(name) { return !!ICONS[name]; }

