#!/usr/bin/env node
/**
 * build-india-map.js — generate data/india-map.js: a tiny inline-SVG source for
 * the "Explore India" section. Reads a GADM India state GeoJSON (35 states/UTs),
 * projects lng/lat → a fixed SVG viewBox, simplifies each polygon ring by
 * point-sampling (keeps the file small; a decorative map doesn't need survey
 * precision), and emits an ES module:
 *
 *   export const INDIA_MAP = { viewBox: "0 0 W H", states: [{ name, path }] };
 *
 * State names are normalized to match data/destinations/index.json meta.states
 * so the map can look up destinations directly. Re-run after replacing the
 * source GeoJSON:  node scripts/build-india-map.js [path/to/source.geojson]
 *
 * Note on data vintage: GADM level-1 predates the 2014/2019 reorganizations, so
 * Telangana is folded into Andhra Pradesh and Ladakh into Jammu & Kashmir on the
 * map outline. Destination lookups still use the current 35-state taxonomy; those
 * two simply share a polygon with their parent state.
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SRC = process.argv[2] || path.join(ROOT, 'india_raw.geojson.tmp');
const OUT = path.join(ROOT, 'data', 'india-map.js');

const W = 560;
const H = 620;
const PAD = 10;
const MAX_PTS_PER_RING = 48; // sampling cap per ring
const MIN_RING_PTS = 40; // ignore rings (islands/slivers) smaller than this

// GADM NAME_1 → dataset meta.states label.
const NAME_MAP = {
  'Andaman and Nicobar': 'Andaman & Nicobar',
  'Daman and Diu': 'Daman & Diu',
  'Jammu and Kashmir': 'Jammu & Kashmir',
  'Orissa': 'Odisha',
  'Uttaranchal': 'Uttarakhand',
  // GADM-only UTs with no dataset destinations — kept on the map as-is:
  'Dadra and Nagar Haveli': 'Dadra & Nagar Haveli',
  'Lakshadweep': 'Lakshadweep',
};
function normName(n) { return NAME_MAP[n] || n; }

function main() {
  if (!fs.existsSync(SRC)) {
    console.error('Source GeoJSON not found:', SRC);
    console.error('Download the GADM India state GeoJSON to that path and re-run.');
    process.exit(1);
  }
  const geo = JSON.parse(fs.readFileSync(SRC, 'utf8'));
  const features = geo.features || [];

  // 1) global bbox for a single shared projection
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
  const scan = (c) => {
    if (typeof c[0] === 'number') {
      if (c[0] < minX) minX = c[0];
      if (c[0] > maxX) maxX = c[0];
      if (c[1] < minY) minY = c[1];
      if (c[1] > maxY) maxY = c[1];
    } else c.forEach(scan);
  };
  features.forEach((f) => scan(f.geometry.coordinates));

  const s = Math.min((W - 2 * PAD) / (maxX - minX), (H - 2 * PAD) / (maxY - minY));
  const offX = PAD + ((W - 2 * PAD) - (maxX - minX) * s) / 2;
  const offY = PAD + ((H - 2 * PAD) - (maxY - minY) * s) / 2;
  const px = (x) => +(offX + (x - minX) * s).toFixed(1);
  const py = (y) => +(offY + (maxY - y) * s).toFixed(1); // flip Y

  // 2) ring → sampled path segment
  function ringPath(ring) {
    if (ring.length < MIN_RING_PTS) return '';
    const step = Math.max(1, Math.floor(ring.length / MAX_PTS_PER_RING));
    const pts = [];
    let last = '';
    for (let i = 0; i < ring.length; i += step) {
      const s2 = px(ring[i][0]) + ' ' + py(ring[i][1]);
      if (s2 !== last) { pts.push(s2); last = s2; } // drop coincident points
    }
    if (pts.length < 3) return '';
    return 'M' + pts.join('L') + 'Z';
  }

  // 3) build one merged path per state. For multipolygon states keep the
  // mainland ring plus any island large enough to read; drop micro slivers.
  const states = [];
  features.forEach((f) => {
    const name = normName(f.properties.NAME_1);
    const g = f.geometry;
    const polys = g.type === 'Polygon' ? [g.coordinates] : g.coordinates;
    // largest ring first so island-only UTs (Andaman, Lakshadweep) keep their biggest piece
    const rings = polys.map((poly) => poly[0]).sort((a, b) => b.length - a.length);
    let d = '';
    rings.forEach((ring, i) => {
      // always keep the biggest ring; for the rest require a readable size
      if (i > 0 && ring.length < MIN_RING_PTS * 1.5) return;
      const seg = ringPath(ring);
      if (seg) d += seg;
    });
    if (d) states.push({ name, path: d });
  });

  states.sort((a, b) => a.name.localeCompare(b.name));

  const header =
    '/**\n' +
    ' * india-map.js — GENERATED by scripts/build-india-map.js. Do not edit by hand.\n' +
    ' * Simplified India state outlines projected to a ' + W + '×' + H + ' SVG viewBox.\n' +
    ' * ' + states.length + ' states/UTs. Names match data/destinations/index.json meta.states.\n' +
    ' */\n';
  const body =
    'export const INDIA_MAP = {\n' +
    '  viewBox: "0 0 ' + W + ' ' + H + '",\n' +
    '  states: [\n' +
    states.map((st) => '    { name: ' + JSON.stringify(st.name) + ', path: ' + JSON.stringify(st.path) + ' }').join(',\n') +
    '\n  ],\n};\n';

  fs.writeFileSync(OUT, header + body);
  const kb = (Buffer.byteLength(header + body) / 1024).toFixed(0);
  console.log('Wrote', path.relative(ROOT, OUT), '—', states.length, 'states,', kb + 'KB');
  console.log('States:', states.map((s) => s.name).join(', '));
}

main();
